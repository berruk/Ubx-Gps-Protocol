typedef struct {        /* time struct */
    time_t time;        /* time (s) expressed by standard time_t */
    double sec;         /* fraction of second under 1 s */
} gtime_t;

const static double gpst0[]={1980,1, 6,0,0,0}; /* gps time reference */

